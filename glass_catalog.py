def compute_refractive_index(glass_name: str, wavelength_um: float) -> float:
    """
    Вычислить показатель преломления по формуле Герцбергера.
    
    n(λ) = C0 + C1*λ^2 + C2*λ^4 + C3*L + C4*L^2 + C5*L^3
    L = 1/(λ^2 - λ0^2)
    """
    if not glass_name or glass_name.strip() == '':
        return 1.0
    
    name = glass_name.upper().strip()
    if name in ('ВОЗДУХ', 'AIR', ''):
        return 1.0
    
    # Try direct lookup
    entry = None
    for key, val in GLASS_CATALOG.items():
        if key.upper() == name:
            entry = val
            break
    
    if entry is None:
        # Try full catalog (889 glasses from .FIL files)
        # Avoid recursion: import the raw function, not our wrapper
        try:
            import glass_catalog_full as _gcf
            cat = _gcf._get_catalog()
            if name in cat or glass_name in cat:
                lookup = glass_name if glass_name in cat else name
                e = cat[lookup]
                coeffs = e.get('coeffs', [])
                if len(coeffs) >= 6:
                    C0, C1, C2, C3, C4, C5 = coeffs[:6]
                    lam = wavelength_um
                    lam0 = LAMBDA0_VISIBLE
                    denom = lam**2 - lam0**2
                    if abs(denom) < 1e-12: denom = 1e-12
                    L = 1.0 / denom
                    return C0 + C1*lam**2 + C2*lam**4 + C3*L + C4*L**2 + C5*L**3
        except Exception:
            pass
        # Unknown glass - return 1.5 as fallback
        return 1.5
    
    nd, vd, coeffs, lam_min, lam_max = entry
    
    if len(coeffs) <= 1:
        return coeffs[0] if coeffs else 1.0
    
    C0, C1, C2, C3, C4, C5 = coeffs[:6]
    lam = wavelength_um
    lam0 = LAMBDA0_VISIBLE
    
    # L = 1/(λ^2 - λ0^2)
    denom = lam**2 - lam0**2
    if abs(denom) < 1e-12:
        denom = 1e-12
    L = 1.0 / denom
    
    n = C0 + C1 * lam**2 + C2 * lam**4 + C3 * L + C4 * L**2 + C5 * L**3
    return n


